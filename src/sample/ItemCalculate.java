package sample;


/**
 * 商品の計算処理
 * 
 * ItemCalculate
 * 
 * @author youhei_yoshikawa
 */
public class ItemCalculate {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int answer = 1 + 2;
		System.out.println(answer);
		
		answer = answer + 3;
		answer = answer - 2;
		answer = answer * 5;
		answer = answer / 2;
		
		System.out.println(answer);
		
		int mod = answer % 3;
		System.out.println(mod);
		
	}

}
